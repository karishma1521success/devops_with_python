# Comparison Operator 

# Integer comparison
#Operator	Description
# -eq	is equal to
# -ne	is not equal to
# -gt	is greater than
# -ge	is greater than or equal to
# -lt	is less than
# -le	is less than or equal to


# String Comparison
# Operator	Description
# ==	is equal to
# !=	is not equal to
# \<	is less than, in ASCII alphabetical order
# \>	is greater than, in ASCII alphabetical order
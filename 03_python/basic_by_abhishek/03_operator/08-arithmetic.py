# Arithmetic operator

a = 4
b=  2

addition = a+b
subtraction = a-b
multiplication = a*b
division = a/b    # normal divide 4/3  = 1.33   - quotient
floor_division = a // b  #  remove all decimal numbers  4/3 = 1
modulus = a % b   # remainder  4/2 = 0  , 4/3 = 1
exponentiation = a ** b   # a ki power b   2 * 3 = 2*2*2 = 8

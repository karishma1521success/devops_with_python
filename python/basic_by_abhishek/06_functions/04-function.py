def add(num1,num2):
    print(num1+num2)    # identation matters to end the function block

def sub(num1,num2):
    print(num1-num2)

def mult(num1, num2):
    print(num1*num2)

def div(num1, num2):
    print(num1/num2)      # 3.0
    print(num1//num2)     #3


#Relational Operator : It returns boolean result either True or False

a = 2
b = 3

a == b   # False  -- Equal to
a != b   # True   -- Not equal to
a > b    # False  -- Greater than
a < b    # True   -- Less than
a >= b   # False  -- Greater than or equal to
a <=b    # True   -- Less than or equal to


